/** Whether date.nager.at carries a public-holiday calendar for
 *  this country. Drives the "Manual entry required" hint on the
 *  sync UI. */
export function isSupportedByNager(code: string): boolean {
  return NAGER_SUPPORTED_CODES.includes(code);
}

/** Display lookup. Returns the ISO code itself when the lookup
 *  fails so a stale tag still renders as something readable. */
export function countryName(code: string): string {
  if (code === 'global') return 'Global';
  return COUNTRIES.find((c) => c.code === code)?.name ?? code;
}

